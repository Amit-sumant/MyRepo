package javaBasic;

public class CodewithoutusingNumber {

	public static void main(String[] args) 
	{
		int one= 'a'/'a'; //here a divided by a = 1
		String b = "..........";
		
		for(int i=one; i<=b.length()*b.length(); i++)
		{
			System.out.println(i);
		}
		
		System.out.println("**************************************************");
		
		//Using ASCII numbers
		
		for(int x=one; x<='d'; x++)
		{
			System.out.println(x);
		}

	}

}
