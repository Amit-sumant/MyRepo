package javaInterviewCoding;

import java.util.Scanner;

public class StringOddEvenNumber {

	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		
		System.out.println("Enter String Characters");
		
		while (true)
		{
		
		String str = sc.next();
		if (str.equalsIgnoreCase("STOP"))
		{
			break;
		}
		
		if (str.length() % 2==0)
		{
			System.out.println("This is Even Number String ->" + str);
			
		}
		else
		{
			System.out.println("This is Odd Number String -> " + str);
		}
		
		}
	}

}
